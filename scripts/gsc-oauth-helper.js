const fs = require("fs");
const http = require("http");
const https = require("https");
const path = require("path");
const crypto = require("crypto");

const root = path.resolve(__dirname, "..");
const credentialsDir = path.join(root, "seo", "gsc", "credentials");
const defaultClientFile = path.join(credentialsDir, "oauth-client.json");
const defaultEnvFile = path.join(credentialsDir, "gsc-env.ps1");
const scope = "https://www.googleapis.com/auth/webmasters.readonly";
const redirectUri = "http://127.0.0.1:53682/oauth2callback";

function argValue(name, fallback) {
  const index = process.argv.indexOf(name);
  return index >= 0 && process.argv[index + 1] ? process.argv[index + 1] : fallback;
}

function requestJson(url, options = {}, body) {
  return new Promise((resolve, reject) => {
    const parsed = new URL(url);
    const req = https.request({
      hostname: parsed.hostname,
      path: `${parsed.pathname}${parsed.search}`,
      method: options.method || "GET",
      headers: options.headers || {}
    }, (res) => {
      let data = "";
      res.setEncoding("utf8");
      res.on("data", (chunk) => { data += chunk; });
      res.on("end", () => {
        let parsedBody = {};
        try {
          parsedBody = data ? JSON.parse(data) : {};
        } catch {
          return reject(new Error(`Invalid JSON response: ${data.slice(0, 500)}`));
        }
        if (res.statusCode < 200 || res.statusCode >= 300) {
          const message = parsedBody.error_description || parsedBody.error?.message || data.slice(0, 500);
          return reject(new Error(`HTTP ${res.statusCode}: ${message}`));
        }
        resolve(parsedBody);
      });
    });
    req.on("error", reject);
    if (body) req.write(body);
    req.end();
  });
}

function loadClient(file) {
  if (!fs.existsSync(file)) {
    throw new Error(`OAuth client file not found: ${file}
Create an OAuth Desktop client in Google Cloud Console, download the JSON, and save it as seo/gsc/credentials/oauth-client.json.`);
  }
  const raw = JSON.parse(fs.readFileSync(file, "utf8"));
  const client = raw.installed || raw.web || raw;
  if (!client.client_id || !client.client_secret) {
    throw new Error("OAuth client JSON must include client_id and client_secret.");
  }
  return { clientId: client.client_id, clientSecret: client.client_secret };
}

function powershellEscape(value) {
  return String(value).replace(/'/g, "''");
}

function writeEnvFile(file, token) {
  const lines = [
    "# Generated by scripts/gsc-oauth-helper.js",
    "# This file is ignored by Git. Dot-source it before running Search Console API pulls.",
    `$env:GSC_CLIENT_ID='${powershellEscape(token.clientId)}'`,
    `$env:GSC_CLIENT_SECRET='${powershellEscape(token.clientSecret)}'`,
    `$env:GSC_REFRESH_TOKEN='${powershellEscape(token.refreshToken)}'`,
    "$env:GSC_SITE_URL='sc-domain:toolsquark.com'",
    ""
  ];
  fs.mkdirSync(path.dirname(file), { recursive: true });
  fs.writeFileSync(file, lines.join("\n"), "utf8");
}

async function waitForCode(expectedState) {
  return new Promise((resolve, reject) => {
    const server = http.createServer((req, res) => {
      const url = new URL(req.url, redirectUri);
      if (url.pathname !== "/oauth2callback") {
        res.writeHead(404);
        res.end("Not found");
        return;
      }
      const state = url.searchParams.get("state");
      const code = url.searchParams.get("code");
      const error = url.searchParams.get("error");
      if (state !== expectedState) {
        res.writeHead(400, { "Content-Type": "text/plain; charset=utf-8" });
        res.end("State mismatch. Return to the terminal and rerun the helper.");
        server.close();
        reject(new Error("OAuth state mismatch."));
        return;
      }
      if (error) {
        res.writeHead(400, { "Content-Type": "text/plain; charset=utf-8" });
        res.end(`Authorization failed: ${error}`);
        server.close();
        reject(new Error(`Authorization failed: ${error}`));
        return;
      }
      res.writeHead(200, { "Content-Type": "text/html; charset=utf-8" });
      res.end("<h1>Search Console authorization complete</h1><p>You can close this tab and return to the terminal.</p>");
      server.close();
      resolve(code);
    });
    server.on("error", reject);
    server.listen(53682, "127.0.0.1");
  });
}

async function exchangeCode(client, code) {
  const params = new URLSearchParams({
    code,
    client_id: client.clientId,
    client_secret: client.clientSecret,
    redirect_uri: redirectUri,
    grant_type: "authorization_code"
  });
  return requestJson("https://oauth2.googleapis.com/token", {
    method: "POST",
    headers: {
      "Content-Type": "application/x-www-form-urlencoded",
      "Content-Length": Buffer.byteLength(params.toString())
    }
  }, params.toString());
}

async function main() {
  if (process.argv.includes("--self-test")) {
    const escaped = powershellEscape("a'b");
    if (escaped !== "a''b") throw new Error("PowerShell escaping failed");
    console.log("GSC OAuth helper self-test passed.");
    return;
  }

  const clientFile = path.resolve(argValue("--client", defaultClientFile));
  const envFile = path.resolve(argValue("--env-file", defaultEnvFile));
  const writeEnv = process.argv.includes("--write-env");
  const client = loadClient(clientFile);
  const state = crypto.randomBytes(18).toString("hex");
  const authUrl = new URL("https://accounts.google.com/o/oauth2/v2/auth");
  authUrl.searchParams.set("client_id", client.clientId);
  authUrl.searchParams.set("redirect_uri", redirectUri);
  authUrl.searchParams.set("response_type", "code");
  authUrl.searchParams.set("scope", scope);
  authUrl.searchParams.set("access_type", "offline");
  authUrl.searchParams.set("prompt", "consent");
  authUrl.searchParams.set("include_granted_scopes", "true");
  authUrl.searchParams.set("state", state);

  console.log("Open this URL in the Google account that owns the Search Console property:");
  console.log(authUrl.toString());
  console.log("");
  console.log("Waiting for the local callback on http://127.0.0.1:53682/oauth2callback ...");

  const code = await waitForCode(state);
  const token = await exchangeCode(client, code);
  if (!token.refresh_token) {
    throw new Error("Google did not return a refresh_token. Revoke the app access in your Google Account, then rerun this helper with prompt=consent.");
  }

  const output = {
    clientId: client.clientId,
    clientSecret: client.clientSecret,
    refreshToken: token.refresh_token
  };

  if (writeEnv) {
    writeEnvFile(envFile, output);
    console.log(`PowerShell env file written: ${envFile}`);
    console.log(`Load it with: . '${envFile}'`);
  } else {
    console.log("Set these variables in PowerShell:");
    console.log(`$env:GSC_CLIENT_ID='${powershellEscape(output.clientId)}'`);
    console.log(`$env:GSC_CLIENT_SECRET='${powershellEscape(output.clientSecret)}'`);
    console.log(`$env:GSC_REFRESH_TOKEN='${powershellEscape(output.refreshToken)}'`);
    console.log("$env:GSC_SITE_URL='sc-domain:toolsquark.com'");
  }
}

if (require.main === module) {
  main().catch((error) => {
    console.error(error.message);
    process.exitCode = 1;
  });
}
